function sum(x, y) {
    return x + y;
  }
  
  function multiply(x, y) {
    return x * y;
  }
  
  function subtract(x, y) {
    return x - y;
  }
  
  function divide(x, y) {
    return x / y;
  }
  
  console.log('Sum', sum(1, 2));
  console.log('Subtract', subtract(1, 2));
  console.log('Divide', divide(1, 2));
  console.log('Multiply', multiply(1, 2));
  